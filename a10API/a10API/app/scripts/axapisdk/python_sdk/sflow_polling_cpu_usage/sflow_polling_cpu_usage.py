def deserialize_Cpu_usage_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	toggle = data.get('toggle')
	result = Cpu_usage(toggle=toggle)
	return result

def serialize_Cpu_usage_json(obj):
	output = OrderedDict()
	if obj.toggle is not None:
		output['toggle'] = obj.toggle
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Cpu_usage_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Cpu_usage_json(item))
	return list(container)


def deserialize_Enable_core_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	a10 = data.get('a10')
	full = data.get('full')
	system = data.get('system')
	result = Enable_core(a10=a10, full=full, system=system)
	return result

def serialize_Enable_core_json(obj):
	output = OrderedDict()
	if obj.a10 is not None:
		output['a10'] = obj.a10
	if obj.full is not None:
		output['full'] = obj.full
	if obj.system is not None:
		output['system'] = obj.system
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Enable_core_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Enable_core_json(item))
	return list(container)


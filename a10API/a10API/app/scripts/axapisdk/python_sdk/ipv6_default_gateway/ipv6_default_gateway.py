def deserialize_Default_gateway_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	ipv6_default_gateway = data.get('ipv6-default-gateway')
	result = Default_gateway(ipv6_default_gateway=ipv6_default_gateway)
	return result

def serialize_Default_gateway_json(obj):
	output = OrderedDict()
	if obj.ipv6_default_gateway is not None:
		output['ipv6-default-gateway'] = obj.ipv6_default_gateway
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Default_gateway_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Default_gateway_json(item))
	return list(container)


import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/admin/access',
]

def deserialize_Access_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	axapi = data.get('axapi')
	cli = data.get('cli')
	web = data.get('web')
	result = Access(axapi=axapi, cli=cli, web=web)
	return result

def serialize_Access_json(obj):
	output = OrderedDict()
	if obj.axapi is not None:
		output['axapi'] = obj.axapi
	if obj.cli is not None:
		output['cli'] = obj.cli
	if obj.web is not None:
		output['web'] = obj.web
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Access_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Access_json(item))
	return list(container)

class Access(AxapiObject):
	__metaclass__ = StructMeta 
	axapi=PosRangedInteger(0, 1)
	cli=PosRangedInteger(0, 1)
	web=PosRangedInteger(0, 1)
	def __init__(self, axapi=None, cli=None, web=None):
		self.axapi = axapi
		self.cli = cli
		self.web = web

	def __str__(self):
		return ""


import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/snmp-server/location',
]

def deserialize_Location_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	loc = data.get('loc')
	result = Location(loc=loc)
	return result

def serialize_Location_json(obj):
	output = OrderedDict()
	if obj.loc is not None:
		output['loc'] = obj.loc
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Location_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Location_json(item))
	return list(container)

class Location(AxapiObject):
	__metaclass__ = StructMeta 
	loc=SizedString(1, 255)
	def __init__(self, loc=None):
		self.loc = loc

	def __str__(self):
		return ""


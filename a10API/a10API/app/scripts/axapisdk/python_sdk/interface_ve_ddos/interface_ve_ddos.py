BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/interface/ve/ddos',
]

def deserialize_Ddos_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	outside = data.get('outside')
	inside = data.get('inside')
	result = Ddos(outside=outside, inside=inside)
	return result

def serialize_Ddos_json(obj):
	output = OrderedDict()
	if obj.outside is not None:
		output['outside'] = obj.outside
	if obj.inside is not None:
		output['inside'] = obj.inside
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Ddos_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Ddos_json(item))
	return list(container)

class Ddos(AxapiObject):
	__metaclass__ = StructMeta 
	outside=PosRangedInteger(0, 1)
	inside=PosRangedInteger(0, 1)
	def __init__(self, outside=None, inside=None):
		self.outside = outside
		self.inside = inside

	def __str__(self):
		return ""


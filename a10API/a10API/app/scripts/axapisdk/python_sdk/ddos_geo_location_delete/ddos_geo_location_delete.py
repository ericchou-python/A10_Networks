def deserialize_Delete_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	delete_file_name = data.get('delete-file-name')
	delete_all = data.get('delete-all')
	result = Delete(delete_file_name=delete_file_name, delete_all=delete_all)
	return result

def serialize_Delete_json(obj):
	output = OrderedDict()
	if obj.delete_file_name is not None:
		output['delete-file-name'] = obj.delete_file_name
	if obj.delete_all is not None:
		output['delete-all'] = obj.delete_all
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Delete_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Delete_json(item))
	return list(container)


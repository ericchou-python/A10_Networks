import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/ipv6/address',
]

def deserialize_Address_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	ipv6_address = data.get('ipv6-address')
	link_local = data.get('link-local')
	anycast = data.get('anycast')
	result = Address(ipv6_address=ipv6_address, link_local=link_local, anycast=anycast)
	return result

def serialize_Address_json(obj):
	output = OrderedDict()
	if obj.ipv6_address is not None:
		output['ipv6-address'] = obj.ipv6_address
	if obj.link_local is not None:
		output['link-local'] = obj.link_local
	if obj.anycast is not None:
		output['anycast'] = obj.anycast
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Address_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Address_json(item))
	return list(container)

class Address(AxapiObject):
	__metaclass__ = StructMeta 
	ipv6_address=SizedString(1, 255)
	link_local=PosRangedInteger(0, 1)
	anycast=PosRangedInteger(0, 1)
	def __init__(self, ipv6_address=None, link_local=None, anycast=None):
		self.ipv6_address = ipv6_address
		self.link_local = link_local
		self.anycast = anycast

	def __str__(self):
		return ""


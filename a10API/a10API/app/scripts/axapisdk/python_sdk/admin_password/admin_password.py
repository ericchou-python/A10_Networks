import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/admin/password',
]

def deserialize_Password_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	password_in_module = data.get('password-in-module')
	encrypted_in_module = data.get('encrypted-in-module')
	result = Password(password_in_module=password_in_module, encrypted_in_module=encrypted_in_module)
	return result

def serialize_Password_json(obj):
	output = OrderedDict()
	if obj.password_in_module is not None:
		output['password-in-module'] = obj.password_in_module
	if obj.encrypted_in_module is not None:
		output['encrypted-in-module'] = obj.encrypted_in_module
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Password_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Password_json(item))
	return list(container)

class Password(AxapiObject):
	__metaclass__ = StructMeta 
	password_in_module=SizedString(1, 63)
	encrypted_in_module=SizedString(1, 255)
	def __init__(self, password_in_module=None, encrypted_in_module=None):
		self.password_in_module = password_in_module
		self.encrypted_in_module = encrypted_in_module

	def __str__(self):
		return ""


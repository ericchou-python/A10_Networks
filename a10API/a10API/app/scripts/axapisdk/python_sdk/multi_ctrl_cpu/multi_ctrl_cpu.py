def deserialize_Multi_ctrl_cpu_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	num_ctrl_cpus = data.get('num-ctrl-cpus')
	result = Multi_ctrl_cpu(num_ctrl_cpus=num_ctrl_cpus)
	return result

def serialize_Multi_ctrl_cpu_json(obj):
	output = OrderedDict()
	if obj.num_ctrl_cpus is not None:
		output['num-ctrl-cpus'] = obj.num_ctrl_cpus
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Multi_ctrl_cpu_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Multi_ctrl_cpu_json(item))
	return list(container)


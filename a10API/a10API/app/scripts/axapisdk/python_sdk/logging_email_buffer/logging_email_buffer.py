def deserialize_Buffer_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	number = data.get('number')
	time = data.get('time')
	result = Buffer(number=number, time=time)
	return result

def serialize_Buffer_json(obj):
	output = OrderedDict()
	if obj.number is not None:
		output['number'] = obj.number
	if obj.time is not None:
		output['time'] = obj.time
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Buffer_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Buffer_json(item))
	return list(container)

class Buffer(AxapiObject):
	__metaclass__ = StructMeta 
	number=PosRangedInteger(16, 256)
	time=PosRangedInteger(10, 1440)
	def __init__(self, number=None, time=None):
		self.number = number
		self.time = time

	def __str__(self):
		return ""


def deserialize_Ve_stats_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	enable = data.get('enable')
	result = Ve_stats(enable=enable)
	return result

def serialize_Ve_stats_json(obj):
	output = OrderedDict()
	if obj.enable is not None:
		output['enable'] = obj.enable
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Ve_stats_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Ve_stats_json(item))
	return list(container)


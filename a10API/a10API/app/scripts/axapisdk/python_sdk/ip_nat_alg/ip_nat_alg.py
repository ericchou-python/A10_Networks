BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/ip/nat/alg',
]

def deserialize_Alg_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	pptp = data.get('pptp')
	result = Alg(pptp=pptp)
	return result

def serialize_Alg_json(obj):
	output = OrderedDict()
	if obj.pptp is not None:
		output['pptp'] = obj.pptp
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Alg_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Alg_json(item))
	return list(container)

class Alg(AxapiObject):
	__metaclass__ = StructMeta 
	pptp = Enum(['disable', 'enable'])
	def __init__(self, pptp=None):
		self.pptp = pptp

	def __str__(self):
		return ""


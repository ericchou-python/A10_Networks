import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/ip/frag',
]

def deserialize_Frag_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	buff = data.get('buff')
	max_reassembly_sessions = data.get('max-reassembly-sessions')
	timeout = data.get('timeout')
	result = Frag(buff=buff, max_reassembly_sessions=max_reassembly_sessions, timeout=timeout)
	return result

def serialize_Frag_json(obj):
	output = OrderedDict()
	if obj.buff is not None:
		output['buff'] = obj.buff
	if obj.max_reassembly_sessions is not None:
		output['max-reassembly-sessions'] = obj.max_reassembly_sessions
	if obj.timeout is not None:
		output['timeout'] = obj.timeout
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Frag_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Frag_json(item))
	return list(container)

class Frag(AxapiObject):
	__metaclass__ = StructMeta 
	buff=PosRangedInteger(10000, 3000000)
	max_reassembly_sessions=PosRangedInteger(1, 200000)
	timeout=PosRangedInteger(4, 16000)
	def __init__(self, buff=None, max_reassembly_sessions=None, timeout=None):
		self.buff = buff
		self.max_reassembly_sessions = max_reassembly_sessions
		self.timeout = timeout

	def __str__(self):
		return ""


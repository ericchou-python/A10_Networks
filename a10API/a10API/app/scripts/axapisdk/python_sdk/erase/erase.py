import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/erase',
]

def deserialize_Erase_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	preserve_management = data.get('preserve-management')
	preserve_accounts = data.get('preserve-accounts')
	reload = data.get('reload')
	result = Erase(preserve_management=preserve_management, preserve_accounts=preserve_accounts, reload=reload)
	return result

def serialize_Erase_json(obj):
	output = OrderedDict()
	if obj.preserve_management is not None:
		output['preserve-management'] = obj.preserve_management
	if obj.preserve_accounts is not None:
		output['preserve-accounts'] = obj.preserve_accounts
	if obj.reload is not None:
		output['reload'] = obj.reload
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Erase_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Erase_json(item))
	return list(container)

class Erase(AxapiObject):
	__metaclass__ = StructMeta 
	preserve_management=PosRangedInteger(0, 1)
	preserve_accounts=PosRangedInteger(0, 1)
	reload=PosRangedInteger(0, 1)
	def __init__(self, preserve_management=None, preserve_accounts=None, reload=None):
		self.preserve_management = preserve_management
		self.preserve_accounts = preserve_accounts
		self.reload = reload

	def __str__(self):
		return ""


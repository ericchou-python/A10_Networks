import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/locale',
]

def deserialize_Locale_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	value = data.get('value')
	test = data.get('test')
	result = Locale(value=value, test=test)
	return result

def serialize_Locale_json(obj):
	output = OrderedDict()
	if obj.value is not None:
		output['value'] = obj.value
	if obj.test is not None:
		output['test'] = obj.test
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Locale_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Locale_json(item))
	return list(container)

class Locale(AxapiObject):
	__metaclass__ = StructMeta 
	value = Enum(['en_US.UTF-8', 'zh_CN.UTF-8', 'zh_CN.GB18030', 'zh_CN.GBK', 'zh_CN.GB2312', 'zh_TW.UTF-8', 'zh_TW.BIG5', 'zh_TW.EUCTW', 'ja_JP.UTF-8', 'ja_JP.EUC-JP'])
	test = Enum(['zh_CN', 'zh_TW', 'ja_JP'])
	def __init__(self, value=None, test=None):
		self.value = value
		self.test = test

	def __str__(self):
		return ""


import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/audit',
]

def deserialize_Audit_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	enable = data.get('enable')
	size = data.get('size')
	privilege = data.get('privilege')
	result = Audit(enable=enable, size=size, privilege=privilege)
	return result

def serialize_Audit_json(obj):
	output = OrderedDict()
	if obj.enable is not None:
		output['enable'] = obj.enable
	if obj.size is not None:
		output['size'] = obj.size
	if obj.privilege is not None:
		output['privilege'] = obj.privilege
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Audit_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Audit_json(item))
	return list(container)

class Audit(AxapiObject):
	__metaclass__ = StructMeta 
	enable=PosRangedInteger(0, 1)
	size=PosRangedInteger(1000, 30000)
	privilege=PosRangedInteger(0, 1)
	def __init__(self, enable=None, size=None, privilege=None):
		self.enable = enable
		self.size = size
		self.privilege = privilege

	def __str__(self):
		return ""


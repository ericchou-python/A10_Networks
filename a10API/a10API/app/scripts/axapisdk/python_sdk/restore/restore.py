import sys
import json
sys.path.append("../common")
from axapi_common import *

BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/restore',
]

def deserialize_Restore_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	use_mgmt_port = data.get('use-mgmt-port')
	remote_file = data.get('remote-file')
	result = Restore(use_mgmt_port=use_mgmt_port, remote_file=remote_file)
	return result

def serialize_Restore_json(obj):
	output = OrderedDict()
	if obj.use_mgmt_port is not None:
		output['use-mgmt-port'] = obj.use_mgmt_port
	if obj.remote_file is not None:
		output['remote-file'] = obj.remote_file
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Restore_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Restore_json(item))
	return list(container)

class Restore(AxapiObject):
	__metaclass__ = StructMeta 
	use_mgmt_port=PosRangedInteger(0, 1)
	remote_file=SizedString(1, 255)
	def __init__(self, use_mgmt_port=None, remote_file=None):
		self.use_mgmt_port = use_mgmt_port
		self.remote_file = remote_file

	def __str__(self):
		return ""


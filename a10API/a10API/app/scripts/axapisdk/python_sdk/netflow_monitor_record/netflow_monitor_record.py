BASE_URL = [
	'https://axapi.a10networks.com/axapi/v3/netflow/monitor/record',
]

def deserialize_Record_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	template = data.get('template')
	result = Record(template=template)
	return result

def serialize_Record_json(obj):
	output = OrderedDict()
	output['template'] = obj.template
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Record_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Record_json(item))
	return list(container)

class Record_template(AxapiObject):
	__metaclass__ = StructMeta 
	template = Enum(['netflow-v5'])
	def __init__(self, template):
		self.template = template

	def __str__(self):
		return str(self.template)

class Record(AxapiObject):
	__metaclass__ = StructMeta 
	template = Enum(['netflow-v5'])
	def __init__(self, template):
		self.template = template

	def __str__(self):
		return str(self.template)


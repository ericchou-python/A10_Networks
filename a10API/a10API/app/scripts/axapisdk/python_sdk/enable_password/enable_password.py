def deserialize_Enable_password_json(obj):
	if obj is None:
		return None
	if isinstance(obj, dict):
		data = obj
	else:
		data = json.loads(obj)
	password = data.get('password')
	encrypted = data.get('encrypted')
	result = Enable_password(password=password, encrypted=encrypted)
	return result

def serialize_Enable_password_json(obj):
	output = OrderedDict()
	if obj.password is not None:
		output['password'] = obj.password
	if obj.encrypted is not None:
		output['encrypted'] = obj.encrypted
	return output

def serialize_final_json(obj):
	return json.dumps(obj)

def deserialize_string_json(obj):
	if obj is None:
		return None
	if isinstance(obj, str):
		return json.loads(obj)
	return obj

def deserialize_list_Enable_password_json(obj):
	if obj is None:
		return None
	if isinstance(obj, list):
		data = obj
	else:
		data = json.loads(obj)
	container = []
	for item in data:
		container.append(deserialize_Enable_password_json(item))
	return list(container)

